class UNet1D(nn.Module):
    def __init__(self, in_channels, base_channels=64):
        super().__init__()
        self.enc1 = nn.Conv1d(in_channels, base_channels, 3, padding=1)
        self.enc2 = nn.Conv1d(base_channels, base_channels*2, 3, padding=1)
        self.dec1 = nn.Conv1d(base_channels*2, base_channels, 3, padding=1)
        self.out = nn.Conv1d(base_channels, in_channels, 3, padding=1)
        self.relu = nn.ReLU()

    def forward(self, x):
        e1 = self.relu(self.enc1(x))
        e2 = self.relu(self.enc2(e1))
        d1 = self.relu(self.dec1(e2))
        return self.out(d1 + e1)

