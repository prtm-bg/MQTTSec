def crop_to_match(x, target):
    if x.size(2) > target.size(2):
        diff = x.size(2) - target.size(2)
        x = x[:, :, :-diff]
    elif x.size(2) < target.size(2):
        diff = target.size(2) - x.size(2)
        pad = (0, diff)
        x = F.pad(x, pad)
    return x


def match_time_dim(a, b):
    """
    Crop or pad tensor a to match the time dimension of tensor b.
    Assumes a and b are in shape [B, C, T].
    """
    T_a = a.shape[2]
    T_b = b.shape[2]

    if T_a > T_b:
        a = a[:, :, :T_b]
    elif T_a < T_b:
        a = F.pad(a, (0, T_b - T_a))

    return a

