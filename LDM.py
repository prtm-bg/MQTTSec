def bhattacharyya_distance(X, Y):
    """
    Compute Bhattacharyya distance between two multivariate distributions.
    X: samples from distribution P (real)
    Y: samples from distribution Q (generated)
    """

    mu_X = np.mean(X, axis=0)
    mu_Y = np.mean(Y, axis=0)

    cov_X = np.cov(X, rowvar=False)
    cov_Y = np.cov(Y, rowvar=False)

    cov_mean = (cov_X + cov_Y) / 2

    # Add small regularization to avoid singular matrix
    eps = 1e-6 * np.eye(cov_mean.shape[0])
    cov_mean += eps
    cov_X += eps
    cov_Y += eps

    diff = mu_X - mu_Y
    term1 = 0.125 * np.dot(np.dot(diff.T, inv(cov_mean)), diff)
    term2 = 0.5 * np.log(det(cov_mean) / np.sqrt(det(cov_X) * det(cov_Y)))

    return term1 + term2

