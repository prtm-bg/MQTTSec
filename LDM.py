def sample_packet_sequence(seed_packet, sequence_len=5, scaler=None):
    """
    Generate synthetic packet sequences using the diffusion model.
    Accepts a scaler argument for flexibility (e.g., gmm_scaler).
    """
    if scaler is None:
        raise ValueError("Scaler must be provided")

    # Ensure correct input format
    if isinstance(seed_packet, pd.Series):
        seed_input = seed_packet.to_numpy().reshape(1, -1)
    elif isinstance(seed_packet, np.ndarray):
        seed_input = seed_packet.reshape(1, -1)
    else:
        raise TypeError("seed_packet must be a pandas Series or numpy array")

    # Scale input
    seed_scaled = scaler.transform(seed_input)

    # Encode and add noise
    z = encoder(torch.tensor(seed_scaled, dtype=torch.float32).repeat(sequence_len, 1)).unsqueeze(0)
    z = z + torch.randn_like(z)

    # Denoising loop
    for _ in range(50):
        noise_pred = diffusion_model(z)
        min_T = min(z.shape[1], noise_pred.shape[1])
        z = z[:, :min_T, :]
        noise_pred = noise_pred[:, :min_T, :]
        z = (z - 0.05 * noise_pred).detach()

    # Decode
    gen_seq = decoder(z).squeeze(0)  # [T, F]
    decoded = gen_seq.detach().numpy()

    # Optional clamp
    decoded = np.clip(decoded, -1000, 1000)

    # Inverse transform
    return scaler.inverse_transform(decoded)



