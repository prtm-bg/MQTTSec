class SeqUNet1D(nn.Module):
    def __init__(self, in_channels=8, base_channels=32):
        super().__init__()

        # Downsampling path (Encoder)
        self.enc1 = nn.Sequential(
            nn.Conv1d(in_channels, base_channels, kernel_size=3, stride=1, padding=1),
            nn.GroupNorm(1, base_channels),
            nn.ReLU()
        )
        self.down1 = nn.Conv1d(base_channels, base_channels, kernel_size=3, stride=2, padding=1)

        self.enc2 = nn.Sequential(
            nn.Conv1d(base_channels, base_channels * 2, kernel_size=3, stride=1, padding=1),
            nn.GroupNorm(1, base_channels * 2),
            nn.ReLU()
        )
        self.down2 = nn.Conv1d(base_channels * 2, base_channels * 2, kernel_size=3, stride=2, padding=1)

        # Bottleneck
        self.mid = nn.Sequential(
            nn.Conv1d(base_channels * 2, base_channels * 4, kernel_size=3, padding=1),
            nn.GroupNorm(1, base_channels * 4),
            nn.ReLU()
        )

        # Upsampling path (Decoder)
        self.up1 = nn.ConvTranspose1d(base_channels * 4, base_channels * 2, kernel_size=2, stride=2)
        self.dec1 = nn.Sequential(
            nn.Conv1d(base_channels * 2, base_channels * 2, kernel_size=3, padding=1),
            nn.GroupNorm(1, base_channels * 2),
            nn.ReLU()
        )

        self.up2 = nn.ConvTranspose1d(base_channels * 2, base_channels, kernel_size=2, stride=2)
        self.dec2 = nn.Sequential(
            nn.Conv1d(base_channels, base_channels, kernel_size=3, padding=1),
            nn.GroupNorm(1, base_channels),
            nn.ReLU()
        )

        self.out = nn.Conv1d(base_channels, in_channels, kernel_size=1)

    def forward(self, x):  # x: [B, T, C]
        x = x.permute(0, 2, 1)  # -> [B, C, T]

        e1 = self.enc1(x)       # [B, base, T]
        d1 = self.down1(e1)     # [B, base, T//2]

        e2 = self.enc2(d1)      # [B, 2base, T//2]
        d2 = self.down2(e2)     # [B, 2base, T//4]

        mid = self.mid(d2)      # [B, 4base, T//4]

        u1 = self.up1(mid)          # [B, C, T']
        e2 = crop_to_match(e2, u1)  # now e2 and u1 have the same T
        cat1 = u1 + e2              # skip connection

        u1 = self.dec1(cat1)

        u2 = self.up2(u1)       # [B, base, T]
        e1 = match_time_dim(e1, u2)
        cat2 = u2 + e1

        u2 = self.dec2(cat2)

        out = self.out(u2)      # [B, in_channels, T]
        out = out.permute(0, 2, 1)  # -> [B, T, in_channels]
        return out


